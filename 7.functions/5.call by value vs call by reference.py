##call  by value
'''
In this particular passing method, the values of the actual parameters copy into the function's formal parameters.
It stores both type of  parameters in different memory locations. Thus , if one makes any changes inside the function
-it does not show on the callers actual parameters.
'''

##call by reference
'''
call by reference is a method in which it passes the reference or address of the actual parameter to the functions formal parameters,
which means if there are any change in the values inside the function, it reflects that change in the actual values.
'''