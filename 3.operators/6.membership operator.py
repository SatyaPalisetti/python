##membership operator
'''used to test if a sequence(e.g.,a string,list,or tuple) contains a specific value
1.in---return true if the value is found in the sequence,otherwise false
2.not in---return true if the value is not found in sequence,false otherwise
'''

x=["Apple","Banana"]
print("Banana" in x)


x = ["apple", "banana"]

print("pineapple" not in x)